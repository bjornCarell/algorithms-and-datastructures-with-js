// ADD and REMOVE from the end
const stack = [];

// add to the end
stack.push('google');
stack.push('youtube');

// remove from the end
stack.pop();

console.log(stack);

// ADD and REMOVE from the beginning
const stack2 = [];

// add to the beginning 
stack2.unshift('resized image');
stack2.unshift('scale image');

// remove from the beginning
stack.shift();

console.log(stack2);

